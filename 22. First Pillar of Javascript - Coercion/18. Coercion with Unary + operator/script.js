//! Coercion with Unary + operator

// In this Unary addition operator it try to convert our other type into only number type let see example of it 
console.log(+"5" , typeof +"5"); //* Output : 5 , number 
console.log(+true, typeof +true); //* Output : 1, number 
console.log(+"abcd", typeof +"abcd"); //* Output : NaN, number